/**
 * @param {Int} a
 * @param {Int} b
 * @returns {Int}
 */
function addInt(a, b) {
    return a.value + b.value
}

/**
 * @param {Float} a
 * @param {Float} b
 * @returns {Float}
 */
function addFloat(a, b) {
    return a.value + b.value
}

/**
 * @param {String} a
 * @param {String} b
 * @returns {String}
 */
function addString(a, b) {
    return a.value + b.value
}
